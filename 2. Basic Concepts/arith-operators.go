package main

import "fmt"

func main() {
  x := 42
  y := 8
      
  // Addition
  add := x + y
  fmt.Println(add)
      
  // Subtraction
  sub = x - y
  fmt.Println(sub)
      
  // Multiplication
  prod = x * y
  fmt.Println(prod)
      
  // Division
  div = x / y
  fmt.Println(div)
      
  // Modulus, results in the remainder of the division
  mod = x % y
  fmt.Println(mod) 

  x += y
  fmt.Println(x)
    
  x *= y
  fmt.Println(x)
}
